CREATE SEQUENCE aut
	MINVALUE 0
	MAXVALUE 99
	START WITH 0
    INCREMENT BY 1
    NOCYCLE;


CREATE OR REPLACE FUNCTION idAut
RETURN CHAR IS
BEGIN
	RETURN 'AT' || aut.nextval;
END;
/

